wchar_t
ucs_from_latin1_char(int ch) {
  unsigned char c = ch;
  if(c >= 0x80)
    return ucs_latin1_table[c & 0x7f];
  return c;
}

char
ucs_to_latin1_char(wchar_t ch) {
  size_t i;
  if(ch < 0x80 || (ch & 0xffffff00) == 0)
    return ch;

  for(i = 0; i < (sizeof(ucs_latin1_table) / sizeof(ucs_latin1_table[0])); ++i) {
    if(ucs_latin1_table[i] == ch)
      return 0x80 + i;
  }
  return 0;
}
